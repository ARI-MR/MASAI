package com.orange.cepheus.cep.model;

/**
 * Model class used by the admin/statments endpoint
 */
public class Statement {

    String name;
    String text;

    public Statement(String name, String text) {
        this.name = name;
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public String getText() {
        return text;
    }
}

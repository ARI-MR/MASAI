package com.orange.cepheus.broker.exception;

import java.security.PrivilegedActionException;

/**
 * Exception that can occur during the persistence of registration.
 */
public class RegistrationPersistenceException extends Exception {


    public RegistrationPersistenceException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Constructs a new exception with the specified cause and a detail
     * message of <tt>(cause==null ? null : cause.toString())</tt> (which
     * typically contains the class and detail message of <tt>cause</tt>).
     * This constructor is useful for exceptions that are little more than
     * wrappers for other throwables (for example, {@link
     * PrivilegedActionException}).
     *
     * @param cause the cause (which is saved for later retrieval by the
     *              {@link #getCause()} method).  (A <tt>null</tt> value is
     *              permitted, and indicates that the cause is nonexistent or
     *              unknown.)
     * @since 1.4
     */
    public RegistrationPersistenceException(Throwable cause) {
        super(cause);
    }
}
